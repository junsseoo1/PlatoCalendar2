class SubjectCodeManager {
  static Map<String, String> _subjectCode = Map.from(_defaultSubjectCode);
  static const String _storageKey = 'subject_code_data';
  static const String _lastUpdateKey = 'subject_code_last_update';
  static const String _googleDocUrl = 'https://docs.google.com/document/d/1i1pVgHBgGccez6AR-QVfKiT7-nzp1UymjMiAL-dZCpU/export?format=txt';
  
  // 과목 코드 맵을 반환하는 getter
  static Map<String, String> get subjectCode => _subjectCode;
  
  // 과목 코드 초기화 및 업데이트
  static Future<void> initialize() async {
    try {
      print("🔄 과목 코드 초기화 시작");
      final prefs = await SharedPreferences.getInstance();
      final savedData = prefs.getString(_storageKey);
      if (savedData != null) {
        try {
          _subjectCode = Map<String, String>.from(json.decode(savedData));
          print("✅ 로컬에서 과목 코드 로드 완료: \\${_subjectCode.length}개");
        } catch (e) {
          print("⚠️ 로컬 데이터 파싱 실패, 기본값 사용: $e");
          _subjectCode = Map.from(_defaultSubjectCode);
        }
      } else {
        print("ℹ️ 로컬 데이터 없음, 구글독스에서 최초 1회 불러오기");
        await _updateFromGoogleDocs();
      }
    } catch (e) {
      print("⚠️ 과목 코드 초기화 실패: $e");
      _subjectCode = Map.from(_defaultSubjectCode);
    }
  }
  
  // 구글 독스에서 과목 코드 업데이트
  static Future<void> _updateFromGoogleDocs() async {
    try {
      print("🔄 구글 독스에서 과목 코드 업데이트 시도");
      
      final response = await http.get(Uri.parse(_googleDocUrl))
          .timeout(Duration(seconds: 10));
      
      if (response.statusCode == 200) {
        final content = response.body;
        final newSubjectCode = _parseSubjectCodeFromText(content);
        
        if (newSubjectCode.isNotEmpty) {
          // 기존 데이터와 비교
          final prefs = await SharedPreferences.getInstance();
          final savedData = prefs.getString(_storageKey);
          
          if (savedData == null || savedData != json.encode(newSubjectCode)) {
            // 데이터가 변경되었거나 처음 로드하는 경우
            _subjectCode = newSubjectCode;
            
            // 로컬에 저장
            await prefs.setString(_storageKey, json.encode(newSubjectCode));
            await prefs.setString(_lastUpdateKey, DateTime.now().toIso8601String());
            
            print("✅ 과목 코드 업데이트 완료: ${_subjectCode.length}개");
            print("📅 마지막 업데이트: ${DateTime.now()}");
          } else {
            print("ℹ️ 과목 코드 변경사항 없음");
          }
        } else {
          print("⚠️ 구글 독스에서 유효한 과목 코드를 찾을 수 없음");
        }
      } else {
        print("⚠️ 구글 독스 접근 실패: ${response.statusCode}");
      }
    } catch (e) {
      print("⚠️ 구글 독스 업데이트 실패: $e");
    }
  }
  
  // 텍스트에서 과목 코드 파싱
  static Map<String, String> _parseSubjectCodeFromText(String text) {
    final Map<String, String> result = {'전체': '전체'};
    
    try {
      // 텍스트를 줄 단위로 분리
      final lines = text.split('\n');
      
      for (String line in lines) {
        line = line.trim();
        
        // 과목 코드 패턴 찾기 (예: 'ZZ7400469': '한국어초급(I)')
        if (line.contains("': '") && line.endsWith("'")) {
          // 작은따옴표로 둘러싸인 부분 추출
          final parts = line.split("': '");
          if (parts.length == 2) {
            final code = parts[0].replaceAll("'", "").trim();
            final name = parts[1].replaceAll("'", "").trim();
            
            if (code.isNotEmpty && name.isNotEmpty) {
              result[code] = name;
            }
          }
        }
        // 다른 패턴도 시도 (예: "ZZ7400469": "한국어초급(I)")
        else if (line.contains('": "') && line.endsWith('"')) {
          final parts = line.split('": "');
          if (parts.length == 2) {
            final code = parts[0].replaceAll('"', "").trim();
            final name = parts[1].replaceAll('"', "").trim();
            
            if (code.isNotEmpty && name.isNotEmpty) {
              result[code] = name;
            }
          }
        }
        // JSON 형식도 시도
        else if (line.contains(':') && line.contains(',')) {
          final colonIndex = line.indexOf(':');
          if (colonIndex > 0) {
            final code = line.substring(0, colonIndex).trim().replaceAll('"', "").replaceAll("'", "");
            final namePart = line.substring(colonIndex + 1).trim();
            final name = namePart.replaceAll('"', "").replaceAll("'", "").replaceAll(',', "").trim();
            
            if (code.isNotEmpty && name.isNotEmpty && code != '전체') {
              result[code] = name;
            }
          }
        }
      }
      
      print("📊 파싱된 과목 코드: ${result.length}개");
      
      // 파싱 결과가 너무 적으면 기본값 사용
      if (result.length < 5) {
        print("⚠️ 파싱된 과목 코드가 너무 적음, 기본값 사용");
        return Map.from(_defaultSubjectCode);
      }
      
    } catch (e) {
      print("⚠️ 과목 코드 파싱 실패: $e");
      return Map.from(_defaultSubjectCode);
    }
    
    return result;
  }
  
